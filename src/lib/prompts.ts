/**
 * Claude Code 記事要約用プロンプト
 * @param articleContent - 要約する記事の内容
 * @returns プロンプト文字列
 */
export function getArticleSummaryPrompt(articleContent: string): string {
  return `あなたは Claude Code（Anthropic の CLI ツール）に関する技術記事の要約を作成するアシスタントです。 記事の内容を正確に伝え、読者が記事を読む価値があるかどうかを判断できるように、客観的な要約を作成してください。

## タスク

以下の記事内容を読んで、要約を作成してください。

## 要約の要件

- 文字数: 150-200文字程度
- 言語: 日本語
- 内容:
    - 記事の主題 (Claude Code のどの機能やトピックを扱っているか)
    - 記事の目的 (読者はこの記事から何を学べるか)
    - 記事の主な内容 (具体的な手順、事例など)
    - 記事のレベル感

## 推奨される表現方法

- 客観的で事実に基づいた記述を使用
- 簡潔で分かりやすい言葉遣いを心がける
- 専門用語は必要に応じて解説を添える
- 具体的な表現を優先: コード例の有無、手順の詳細度を明記
- 実用性を重視: 読者が実際に活用できる内容かどうかを示す
- 建設的な情報提供: 記事から得られる具体的な価値を明確に表現

## 重点項目

1. Claude Code の機能や使い方
2. 実装方法やコード例の有無
3. 実際の活用事例や TIPS の有無

## 出力要件

- プレーンテキスト形式で出力
- 句読点や記号を除いた文字数で150-200文字程度に調整
- 要約のみを出力し、追加の解説や前置きは含めない

## 記事内容

${articleContent}

上記の指示に基づいて要約を作成してください。`;
}

/**
 * 週間レポート全体総括用プロンプト
 */
export function getOverallSummaryPrompt(
  summaries: Array<{
    articleId: string;
    summary: string;
  }>,
): string {
  const summaryTexts = summaries.map((s) => `${s.summary}`).join('\n');

  return `あなたは Claude Code（Anthropic の CLI ツール）に関する週間技術トレンドを分析するアシスタントです。今週注目された記事群から、週全体のトレンドや傾向を総括してください。

## タスク

以下の今週の人気記事要約を読んで、週全体の総括文章を作成してください。

## 総括の要件

- 文字数: 200-300文字程度
- 言語: 日本語
- 内容:
  - 今週のClaude Code関連記事のトレンド
  - 注目された機能やトピック
  - 開発者コミュニティの関心事
  - 実用的な活用事例の傾向

## 推奨される表現方法

- 客観的で俯瞰的な視点を採用
- 具体的なトレンドを数値や事例と共に表現
- 読者にとって有用な情報を優先
- 今後の展望や学習方向性への示唆を含める

## 出力要件

- プレーンテキスト形式で出力
- 総括文章のみを出力し、追加の解説や前置きは含めない
- 引用箇所を(1,2)といった番号で提示しない
- 太字（**）やその他の装飾記号は使用しない
- 個別記事への言及や参照番号は含めない

## 今週の記事要約一覧

${summaryTexts}

上記の指示に基づいて週間総括を作成してください。`;
}
